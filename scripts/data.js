window.getCustomInventory = function () {
  try {
    const stored = localStorage.getItem("customInventory");
    return stored ? JSON.parse(stored) : [];
  } catch (error) {
    console.warn("Unable to access localStorage", error);
    return [];
  }
};

window.saveCustomInventory = function (cars) {
  try {
    localStorage.setItem("customInventory", JSON.stringify(cars));
  } catch (error) {
    console.warn("Unable to save to localStorage", error);
  }
};
